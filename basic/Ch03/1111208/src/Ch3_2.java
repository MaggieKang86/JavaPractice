public class Ch3_2 {

    public static void main(String[] args) {
        //布林值表示true or false
        boolean b1 = true;
        boolean b2 = false;
        System.out.println(b1);
        System.out.println(b2);

        //常數：不可變動的數值
        final double PI = 3.1415967;
        double angle = Math.sin(PI / 2) * 180 / PI;
        System.out.println(angle);
    }

}
